// ----------------------------------------------------------
// Tool definition
// ----------------------------------------------------------
export const legalDrafterTool: Tool = {
  name: "get_legal_template",
  description:
    "Provides structured Omani legal document templates to assist in drafting. " +
    "Use this to get the correct format for rulings, legal memos, or contracts " +
    "before filling in the specifics.",

  parameters: {
    type: "object",
    required: ["templateType"],
    properties: {
      templateType: {
        type: "string",
        enum: ["ruling", "legal_memo", "contract"],
        description:
          "Type of document: 'ruling' (حكم قضائي), 'legal_memo' (مذكرة قانونية), 'contract' (عقد)",
      },
    },
  },

  execute: async (args, _env: Env): Promise<string> => {
    const { templateType } = args;
    const template = TEMPLATES[templateType];

    if (!template) {
      return JSON.stringify({
        error: "UNKNOWN_TEMPLATE",
        available: Object.keys(TEMPLATES),
        message: `نوع القالب غير معروف: ${templateType}`,
      });
    }

    return JSON.stringify({
      templateType,
      template,
      instructions:
        "يمكنك تعديل هذا القالب وملء الحقول المحددة بين الأقواس المربعة [ ]",
    });
  },
};
